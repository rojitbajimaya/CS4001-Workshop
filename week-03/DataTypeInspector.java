
public class DataTypeInspector{
    
    public static void main(String[] args){
    byte byteNum = 5;
    short shortNum = 120;
    long longNum = 5000l;
    int intNum = 10000;
    float floatNum = 2.5632f;
    double doubleNum = 3.4566664d;
    char character='A';
    boolean flag = true;
    
    System.out.println("This is a byte number: " + byteNum);
    System.out.println("This is a short number: " + shortNum);
    System.out.println("This is a long number: " + longNum);
    System.out.println("This is a integer number: " + intNum);
    System.out.println("This is a float number: " + floatNum);
    System.out.println("This is a double number: " + doubleNum);
    System.out.println("This is a char: " + character);
    System.out.println("This is a boolean: " + flag);
    
    }
}